# Печатаем заголовок таблицы умножения
print("Таблица умножения на 5:")
print("------------------------")

# Используем цикл for для прохода от 1 до 10
for i in range(1, 11):
    # Вычисляем результат умножения
    result = 5 * i

    # Печатаем запись в таблице умножения
    print("5 *", i, "=", result)

# Печатаем разделитель таблиц
print("------------------------")